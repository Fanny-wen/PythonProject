"""
变量作用域指的是变量生效的范围，主要分为两类：局部变量和全局变量
"""


# 局部变量
# 所谓局部变量是定义在函数体内部的变量，即只在函数体内部生效
def test_a():
    a = 100
    print(a)


test_a()  # 100
print(a)  # NameError: name 'a' is not defined

'''
变量a是定义在test_a函数内部的变量，在函数外部访问则立即报错。
局部变量的作用：在函数体内部，临时保存数据，即当函数调用完成后，则销毁局部变量。
'''

