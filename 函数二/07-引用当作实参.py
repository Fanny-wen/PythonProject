"""
需求：引用是否可以当作实参
1.定义函数：有形参
    1.1访问打印形参看是否有数据
    1.2访问形参的id
    1.3改变形参的数据，查看这个形参并打印id，看id值是否相同
2。调用函数  --- 把可变和不可变两种类型依次当作实参传入
"""


def test_1(a):
    print(a)
    print(id(a))  # 140732715417520

    a += a
    print(a)
    print(id(a))  # 140732715420720
    """两次id值不同"""


b = 100
test_1(b)

c = [1, 2, 3, 4]
test_1(c)
